// Detect stored language (persist between visits). Default is Armenian 'hy'.
let initialLang = 'hy';
try {
  const stored = typeof window !== 'undefined' ? localStorage.getItem('app_language') : null;
  if (stored && (stored === 'ru' || stored === 'hy' || stored === 'en')) initialLang = stored;
} catch (e) {
  // ignore
}

i18n
  .use(initReactI18next)
  .init({
    resources,
    lng: initialLang,
    fallbackLng: 'hy',
    supportedLngs: ['hy','ru','en'],
    preload: ['hy','ru','en'],
    defaultNS: 'translation',
    react: { useSuspense: false },
    interpolation: {
      escapeValue: false
    }
  });

// Persist language changes
i18n.on('languageChanged', (lng) => {
  try {
    localStorage.setItem('app_language', lng);
  } catch (e) {
    // ignore storage errors
  }
});

