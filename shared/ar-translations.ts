// Helper function to get translation
export function getARTranslation(lang: 'hy' | 'ru' | 'en', key: string): string {
  const keys = key.split('.');
  let value: any = arTranslations[lang];
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  return value || key;
}

// Helper to format price with currency
